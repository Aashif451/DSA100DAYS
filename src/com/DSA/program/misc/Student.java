package com.DSA.program.misc;

public class Student {
	
	
	private int age ;
	
	private String name;
	
	private double salary;

	public Student(int age, String name, double salary) {
		super();
		this.age = age;
		this.name = name;
		this.salary = salary;
	}

	public int getAge() {
		return age;
	}

	public String getName() {
		return name;
	}

	public double getSalary() {
		return salary;
	}
	
	
	

}
